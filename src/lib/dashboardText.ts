export function formatDashboardDate(value: string | Date | null | undefined, locale: string, unknownDate: string): string {
  if (value === null || value === undefined) {
    return unknownDate;
  }

  const date = new Date(String(value));
  if (Number.isNaN(date.getTime())) {
    return unknownDate;
  }

  return new Intl.DateTimeFormat(locale, {
    year: "numeric",
    month: "short",
    day: "2-digit",
  }).format(date);
}
